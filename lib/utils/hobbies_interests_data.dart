  /// Busca hobbies que correspondem ao termo de busca
  static List<Map<String, String>> searchHobbies(String query) {
    if (query.isEmpty) return [];
    final lowerQuery = query.toLowerCase();
    return hobbies.where((hobby) {
      return hobby['name']!.toLowerCase().contains(lowerQuery);
    }).toList();
  }

  /// Retorna todos os hobbies
  static List<Map<String, String>> getAllHobbies() {
    return List.from(hobbies);
  }

