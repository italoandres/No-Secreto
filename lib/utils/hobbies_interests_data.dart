  /// Verifica se um hobby existe na lista
  static bool isValidHobby(String hobbyName) {
    return hobbies.any((hobby) => hobby['name'] == hobbyName);
  }

  /// Retorna o emoji de um hobby específico
  static String? getHobbyEmoji(String hobbyName) {
    final hobby = hobbies.firstWhere(
      (h) => h['name'] == hobbyName,
      orElse: () => {'name': '', 'emoji': ''},
    );
    return hobby['emoji']?.isNotEmpty == true ? hobby['emoji'] : null;
  }
